public class P36_StringCharAtExample {
    public static void main(String[] args) {
        String str = "Java Programming";
        System.out.println("第1個字元: " + str.charAt(0));
        System.out.println("第2個字元: " + str.charAt(1));
        System.out.println("字串長度: " + str.length());
    }
}
// 第1個字元: J \n 第2個字元: a \n 字串長度: 16